/* global
    hash : true,
    filterGame : true,
    filterAction,
    render_new_members,
    render_all_members,
    render_popular_members
*/
'use strict';

$(window).on('hashchange', function(){
    hash = window.location.hash.replace('#!/', '');
    hash = hash.split('/');
    if(!~hash.indexOf('game')) {
        filterGame = '';
        render_new_members();
        render_all_members();
        render_popular_members();
    }
    filterAction(hash.shift());
});
